    <p style="line-height: 2em; font-size: 8pt"><span style="font-size: 19px; ">El grupo de investigación en Ambientes Inteligentes Adaptativos GAIA, está avalado institucionalmente por la Universidad Nacional de Colombia y reconocido por Colciencias. Fue creado en el año 2001, orientado a la Seguridad y Auditoría de Sistemas, y en el 2005 tomó elactual nombre e incorporó nuevas líneas de investigación permitiendo la vinculación de nuevos investigadores. <br>Actualmente es un grupo multidisciplinario conformado por profesionales orientados a la investigación en Tecnologías de la Información y Comunicación TICs tanto en la fundamentación como en las aplicaciones en problemas del mundo real. <br><br>Durante estos años, sus integrantes han participado en proyectos conjuntos financiados por entidades nacionales e internacionales. La vinculación de los investigadores con pares de países como Brasil, Chile, Argentina, Uruguay, Cuba, Costa Rica, España, Holanda, Italia, entre otros, ha permitido la cooperación académica con diferentes instituciones.<br>El uso de las técnicas inteligentes y la visión de modelos y sistemas adaptables es una fortaleza del grupo.</span> </p>


